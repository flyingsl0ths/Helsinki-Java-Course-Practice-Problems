public class VaryingVariables
{
    public static void main(String[] args)
    {
        int numOfChickens = 9000;
        double amountOfBacon = 0.1;
        String typeOfTractor = "Zetor";
        
        System.out.println("Chickens:\n" + numOfChickens);
        System.out.println("Bacon(kg):\n" + amountOfBacon);
        System.out.println("A tractor:\n" + typeOfTractor + "\n");
        
        System.out.println("In a nutshell:");
        System.out.println(numOfChickens);
        System.out.println(amountOfBacon);
        System.out.println(typeOfTractor);
    }
}